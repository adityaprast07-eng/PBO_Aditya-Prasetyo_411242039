    private void jTable3MouseClicked(java.awt.event.MouseEvent evt) {                                     
        // TODO add your handling code here:
        int baris = jTable3.getSelectedRow();

        // Mengambil data dari kolom tabel dan dimasukkan ke TextField
        jTextField1.setText(jTable3.getValueAt(baris, 0).toString());
        jTextField2.setText(jTable3.getValueAt(baris, 1).toString());

        // Kunci jTextField1 (ID Jenis) agar tidak bisa diubah karena merupakan Primary Key
        jTextField1.setEditable(false);
    }                                    

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        int konfirmasi = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin ingin menghapus data ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

        if (konfirmasi == JOptionPane.YES_OPTION) {
            try {
                stat = con.createStatement();
                String sql = "DELETE FROM jenis_buku WHERE id_jenis='" + jTextField1.getText() + "'";

                stat.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus!");

                tampilData();
                bersihForm();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Gagal menghapus data: " + e.getMessage());
                e.printStackTrace();
            }
        }
    }                                        

