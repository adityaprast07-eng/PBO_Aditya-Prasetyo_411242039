    public void tampilData() {
        DefaultTableModel isitabel = new DefaultTableModel();
        isitabel.addColumn("ID Jenis");
        isitabel.addColumn("Jenis Buku");

        // TAMBAHKAN BARIS INI untuk memastikan tabel kosong sebelum memuat data baru
        isitabel.setRowCount(0);

        try {
            stat = con.createStatement();
            res = stat.executeQuery("select * from jenis_buku");
            while (res.next()) {
                isitabel.addRow(new Object[]{res.getString("id_jenis"), res.getString("jenis_buku")});
            }
            jTable3.setModel(isitabel); // Memasukkan model yang fresh ke jTable
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal menampilkan data: " + e.getMessage());
            e.printStackTrace();
        }
    }
    
